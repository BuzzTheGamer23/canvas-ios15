import Foundation
import CalendarKit

extension UIColor {
    public static func parentRedColor() -> UIColor {
        return UIColor(r: 208, g: 2, b: 27)
    }

    public static func parentYellowColor() -> UIColor {
        return UIColor(r: 245, g: 166, b: 35)
    }

    public static func parentBlueColor() -> UIColor {
        return UIColor(r: 0, g: 150, b: 219)
    }

    public static func parentGreenColor() -> UIColor {
        return UIColor(r: 72, g: 175, b: 73)
    }

    public static func parentLightGreyColor() -> UIColor {
        return UIColor(r: 210, g: 210, b: 210)
    }

    public static func defaultTableViewBackgroundColor() -> UIColor {
        return UIColor(r: 240, g: 239, b: 238)
    }
}